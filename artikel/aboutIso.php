                        <h4>Belajar Fotografi</h4>
                        <h2>Pengertian ISO dalam Fotografi</h2>

                        <div>

<p>Ketika kita akan membahas berkaitan dengan ISO tentunya tidak akan jauh-jauh dari yang namanya fotografi, karena memang istilah ISO banyak akan kita temukan di fotografi pada khususnya. Secara harfiah atau dari arti secara umum, ISO merupakan kemampuan yang dimiliki oleh kamera dalam menangkap sebuah cahaya. Bisa kita katakan kalau ISO merupakan sisi sensitif dari sebuah kamera. Ketika kita akan menggunakan pengaturan ISO, tentunya kita juga harus paham berkaitan dengan fungsi dasar dalam penggunaan ISO. Ketika kita akan mengatur ISO yang semakin besar, maka cahaya yang ditangkap oleh kamera akan menjadi semakin besar dan kamera menjadi lebih sensitif. Fitur pengaturan ISO sendiri merupakan bagian dri segitiga exposure dalam sebuah kamera selain shutter speed dan juga aperture.</p>

<img src="img/iso1.jpg" class="blog_item">

<p>Pengertian ISO</p>

<p>Dalam dunia sehari-hari kita akan bisa mengumpamakan ISO adalah sebuah kerikil atau batu yang kita masukkan ke dalam segelas air. Tentunya dengan bantuan dari batu gelas tersebut tidak memerlukan banyak air untuk bisa menjadi penuh dan air sampai ke bibir gelas tanpa harus kita menuangkan air sampai ke bibir gelas sendiri. Nah, perumpaman ini bisa kita samakan dengan cara kerja dan fungsi dari ISO itu sendiri. Dengan semakin besar ISO yang akan kita gunakan nanti, tentunya akan membuat kita tidak memerlukan banyak cahaya untuk bisa mendapatkan cahaya yang tepat dan juga ketepatan cahaya untuk bisa mendapatkan gambar terbaik dengan menggunakan kamera.</p>

<img src="img/iso2.jpg" class="blog_item">

<p>Setelah kita membahas berkaitan ISO secara umum dan juga fungsi dari ISO itu sendiri. Ada banyak hal lain berkaitan dengan ISO yang juga harus kita pahami dalam dunia fotografi. Salah satu hal yang akan kita temukan dalam pengaturan ISO tentunya adalah satuan dari ISO itu sendiri. Untuk satuan yang ada di dalam pengaturan ISO bukan hanya ada pilihan auto saja. Namun, masih ada pilihan angka yang akan dimulai dari 50/100, 200, 400, 800, 1600 dan masih ada selanjutnya dalam satuan ISO. Kalau kita menggunakan kamera yang sudah profesional, maka untuk nilai satuan dari ISO sendiri akan bisa mencapai sampai 25000. ISO bukan hanya akan membuat pengambilan gambar dengan cahaya yang tepat mudah untuk kita lakukan saja, namun dengan menggunakan ISO juga akan menimbulkan efek noise pada foto atau gambar yang kita dapatkan. Dengan semakin canggihnya kamera yang ada sekarang, efek yang akan ditimbulkan dari ISO yaitu noise juga bukan menjadi masalah yang besar lagi karena banyak kamera yang walaupun menggunakan ISO dengan satuan yang tinggi masih bisa mendapatkan efek noise pada foto yang sangat rendah.</p>

<img src="img/iso3.jpg" class="blog_item">

<p>Ketika membahas berkaitan dengan ISO tentunya juga memang tidak bisa kita lepaskan dari yang namanya efek noise dari ISO. Seperti yang telah diuraikan diatas, memang dengan semakin tingginya ISO yang akan kita gunakan, maka noise yang nantinya akan bisa kita dapatkan akan menjadi semakin tinggi. Namun, masalah noise ini juga bukan menjadi masalah penggunaan ISO tinggi karena sudah banyak kamera canggih yang bisa mengurangi efek noise pada penggunaan ISO. Nah, mari kita bahas lebih jauh berkaitan dengan noise. Bagi yang masih belum paham dengan apa itu noise, maka berikut ini penjelasan untuk pengertian noise. Noise merupakan bintik kecil yang akan bisa kita temukan di dalam foto. Efek noise memang salah satunya bisa terjadi karena penggunaan ISO. Penggunaan ISO yang kurang tepat juga akan membuat foto menjadi tidak bagus kualitasnya.</p>

<p>Dengan kita sudah paham dengan pengertian ISO kamera dan juga efek yang terjadi dari penggunaan ISO yang ada diatas. Maka kapan kita akan menggunakan ISO dengan tepat. Untuk ISO dengan nilai yang tinggi biasanya akan bisa kita gunakan pada saat kita akan memotret dengan intensitas cahaya yang kurang. Artinya ISO tinggi digunakan kalau kita memotret pada saat malam hari misalnya. Intinya dalam menentukan ISO yang tepat tentunya kita juga harus melakukan pengaturan pada exposure speed dan juga aperture juga.</p>


