                        <h4>Belajar Fotografi</h4>
                        <h2>Teknologi Sensor Kamera Mirrorless</h2>

                        <div>

<p>Salah satu aspek yang dilihat saat menilai kualitas kamera digital adalah sensornya. Kita tahu sensor pada kamera digital adalah rangkaian peka cahaya, tempat gambar dibentuk dan dirubah menjadi sinyal data. Tidak semua kamera digital punya ukuran sensor yang sama. Sesuai bentuknya, kamera digital yang kecil umumnya pakai sensor yang juga kecil, sedangkan kamera mirrorless dan DSLR memakai sensor yang lebih besar. Sensor dengan luas penampang sama dengan ukuran film 35mm disebut sensor full frame. Mengapa penting untuk mengenal ukuran sensor di kamera digital? Karena ukuran sensor berkaitan dengan kemampuan menangkap cahaya dan menentukan bagus tidaknya hasil foto yang diambil. Sekeping sensor pada dasarnya merupakan sekumpulan piksel yang peka cahaya, saat ini umumnya sekeping sensor punya 10 juta piksel bahkan lebih. Makin banyak piksel, makin detil foto yang bisa direkam. Tapi saat bicara kualitas hasil foto, kita perlu mencari lebih jauh info ukuran sensornya, bukan sekedar berapa juta pikselnya saja.</p>

<img src="img/sensor1.jpg" class="blog_item">

<p>Megapiksel, atau resolusi sensor, saat ini seperti jadi cara efektif untuk marketing. Maka itu ponsel berkamera pun dibuat punya sensor yang megapikselnya tinggi. Pun demikian dengan kamera saku sampai kamera canggih, semua berlomba menjual ‘megapiksel’ ini. Bayangkan sensor kecil yang dijejali piksel begitu banyak, seperti apa rapat dan sempitnya piksel-piksel itu berhimpit? Dibawah ini adalah contoh ilustrasi ukuran sensor, dua di sebelah kiri (yang berwarna merah) adalah mewakili sensor kecil, umumnya ditemui di kamera saku. Sensor kecil memang murah dalam hal biaya produksi, dan bisa membuat bentuk kamera jadi sangat kecil.</p>

<img src="img/sensor15.jpg" class="blog_item">

<p>Di sisi lain, ukuran sensor yang lebih besar memang lebih mahal dan kamera/lensanya jadi lebih besar. Tapi keuntungannya dengan luas penampang yang lebih besar, tiap piksel punya ukuran yang lebih besar dan mampu menangkap cahaya dengan lebih baik. Maka itu saat kondisi kurang cahaya, dimana kamera tentu akan menaikkan ISO (kepekaan sensor), yang terjadi adalah hasil foto dari kamera dengan sensor besar punya hasil foto yang lebih baik. Sedangkan di ISO tinggi, kamera sensor kecil akan dipenuhi bercak noise yang mengganggu. Noise ini oleh kamera modern dicoba untuk dikurangi secara otomatis (lewat prosesor kamera) namun yang terjadi hasil fotonya jadi tidak natural seperti lukisan cat air.</p>
<p>Sensor CMOS vs sensor CCD</p>
<p>Perbedaan utama desain CMOS dan CCD adalah pada sirkuit digitalnya. Setiap piksel pada sensor CMOS sudah memakai sistem chip yang langsung mengkonversi tegangan menjadi data, sementara piksel-piksel pada sensor CCD hanya berupa photodioda yang mengeluarkan sinyal analog (sehingga perlu rangkaian terpisah untuk merubah dari analog ke digital/ADC). Anda mungkin penasaran mengapa banyak produsen yang kini beralih ke sensor CMOS, padahal secara hasil foto sensor CCD juga sudah memenuhi standar. Alasan utamanya menurut saya adalah soal kepraktisan, dimana sekeping sensor CMOS sudah mampu memberi keluaran data digital siap olah sehingga meniadakan biaya untuk membuat rangkaian ADC.</p>

<img src="img/sensor2.png" class="blog_item">

<p>Selain itu sensor CMOS juga punya kemampuan untuk diajak bekerja cepat yaitu sanggup mengambil banyak foto dalam waktu satu detik. Ini tentu menguntungkan bagi produsen yang ingin menjual fitur high speed burst. Faktor lain yang juga perlu dicatat adalah sensor CMOS lebih hemat energi sehingga pemakaian baterai lebih awet. Maka itu tak heran kini semakin banyak kamera digital (DSLR maupun kamera saku) yang akhirnya beralih ke sensor CMOS. Adapun soal kemampuan sensor CMOS dalam ISO tinggi pada dasarnya tak berbeda dengan sensor CCD dimana noise yang ditimbulkan juga linier dengan kenaikan ISO. Kalau ada klaim sensor CMOS lebih aman dari noise maka itu hanya kecerdikan produsen dalam mengatur noise reduction.</p>
<p>Cara sensor ‘menangkap’ warna</p>

<img src="img/sensor3.jpg" class="blog_item">

<p>Sensor gambar pada dasarnya merupakan perpaduan dari chip peka cahaya (untuk mendapat informasi terang gelap) dan filter warna (untuk merekam warna seakurat mungkin). Di era fotografi film, pada sebuah roll film terdapat tiga lapis emulsi yang peka terhadap warna merah (Red), hijau (Green) dan biru (Blue). Di era digital, sensor kamera memiliki bermacam variasi desain teknologi filter warna tergantung produsennya dan harga sensornya. Cara kerja filter warna cukup simpel, misal seberkas cahaya polikromatik (multi warna) melalui filter merah, maka warna apapun selain warna merah tidak bisa lolos melewati filter itu. Dengan begitu sensor hanya akan menghasilkan warna merah saja. Untuk mewujudkan jutaan kombinasi warna seperti keadaan aslinya, cukup memakai tiga warna filter yaitu RGB (sama seperti film) dan pencampuran dari ketiga warna komplementer itu bisa menghasilkan aneka warna yang sangat banyak. Hal yang sama kita bisa jumpai juga di layar LCD seperti komputer atau ponsel yang tersusun dari piksel RGB.</p>

<img src="img/sensor4.gif" class="blog_item">

<p>Bayer CFA</p>
<p>Sesuai nama penemunya yaitu Bryce Bayer, seorang  ilmuwan dari Kodak pertama kali memperkenalkan teknik ini di tahun 1970. Sensor dengan desain Bayer Color Filter Array (CFA) termasuk sensor paling banyak dipakai di kamera digital hingga saat ini. Keuntungan desain sensor Bayer adalah desain mosaik filter warna yang simpel cukup satu lapis, namun sudah mencakup tiga elemen warna dasar yaitu RGB (lihat ilustrasi di atas). Kerugiannya adalah setiap satu piksel pada dasarnya hanya ‘melihat’ satu warna, maka untuk bisa menampilkan warna yang sebenarnya perlu dilakukan teknik color sampling dengan perhitungan rumit berupa interpolasi (demosaicing). Perhatikan ilustrasi mosaik piksel di bawah ini, ternyata filter warna hijau punya jumlah yang lebih banyak dibanding warna merah dan biru. Hal ini dibuat mengikuti sifat mata manusia yang lebih peka terhadap warna hijau.</p>

<img src="img/sensor5.png" class="blog_item">

<p>Kekurangan sensor Bayer yang paling disayangkan adalah hasil foto yang didapat dengan cara interpolasi tidak bisa menampilkan warna sebaik aslinya. Selain itu kerap terjadi moire pada saat sensor menangkap pola garis yang rapat seperti motif di kemeja atau pada bangunan. Cara termudah mengurangi moire adalah dengan memasang filter low pass yang bersifat anti aliasing, yang membuat ketajaman foto sedikit menurun.</p>
<p>Sensor X Trans.</p>
<p>Sensor dengan nama X Trans dikembangkan secara ekslusif oleh Fujifilm, dan digunakan pada beberapa kamera kelas atas Fuji seperti X-E2 dan X-T1. Desain filter warna di sensor X Trans merupakan pengembangan dari desain Bayer yang punya kesamaan bahwa setiap piksel hanya bisa melihat satu warna. Bedanya, Fuji menata ulang susunan filter warna RGBnya. Bila pada desain Bayer kita menemui dua piksel hijau, satu merah dan satu biru pada grid 2×2, maka di sensor X Trans kita akan menemui pola grid 6×6 yang berulang. Nama X trans sepertinya diambil dari susunan piksel hijau dalam grid 6×6 yang membentuk huruf X seperti contoh di bawah ini.</p>

<img src="img/sensor6.jpg" class="blog_item">

<p>Sepintas kita bisa setuju kalau desain X Trans lebih baik daripada Bayer, namun ada beberapa hal yang masih jadi kendala dari desain X Trans ini, yaitu hampir tidak mungkin Fuji akan memberikan lisensi X Trans ke produsen kamera lain (artinya hanya pemilik kamera Fuji tipe tertentu yang bisa menikmati sensor ini). Kendala lain adalah sulitnya dukungan aplikasi editing untuk bisa membaca file RAW dari sensor X Trans ini.</p>

