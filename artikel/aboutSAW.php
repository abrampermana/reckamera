                        <h4>Decison Making System</h4>
                        <h2>Apa itu metode Simple Additive Weighting?</h2>

                        <div>

<p>Dalam merekomendasikan kamera Mirroless untuk kamu, sistem yang dibangun di website ini menggunakan salah satu metode ilmiah dalam Decision Making System, yaitu metode Simple Additive Weighting. Secara garis besar, metode ini membandingkan beberapa kriteria yang selalu ada dari setiap kamera mirrorless dan menghasilkan nilai akhir sesuai dengan perhitungan metode ini. Metode ini sudah banyak digunakan dalam banyak kasus Decision Making System, dan sangat membantu banyak orang dalam menentukan pilihan yang terbaik.</p>

<p>Simple Additive Weighting adalah salah satu dari beberapa metode Sistem Pendukung Keputusan (Decison Making System) yang digunakan untuk mencari alternatif optimal dari sejumlah alternatif dengan kriteria tertentu. Metode ini mengharuskan pembuat keputusan menentukan bobot bagi setiap atribut.</p>

<p>Langkah penyelesaian metode Simple Aditive Weighting dimulai dari : </p>

<p> 1. Menentukan kriteria yang akan dijadikan acuan dalam pengambilan keputusan</br>
    2. Menentukan rating kecocokan setiap alternatif pada setiap kriteria.</br>
    3. Membuat matriks keputusan berdasarkan kriteria , kemudian melakukan normalisasi matriks berdasarkan persamaan persamaan yang disesuaikan dengan jenis atribut (atribut keuntungan atau atribut biaya).</br>
    4. Membuat matriks normalisasi.</br>
    5. Proses perankingan.</p>

<p>Perhitungan metode Simple Aditive Weighting dihitung dengan :</p>

<p>1. Formula untuk melakukan normalisasi:</p>

<img src="img/saw1.png" class="blog_item">

<p>Dengan :</br>
Rij	: Rating kinerja ternormalisasi</br>
Maxi	: Nilai maksimum dari setiap baris dan kolom</br>
Mini	: Nilai minimum dari setiap baris dan kolom</br>
Xij	: Baris dan kolom dari matriks</br>
Rij adalah rating kinerja ternormalisasi dari alternatif Ai pada atribut Ci</p>

<p>2.Formula untuk menghitung nilai preferensi :</p>

<img src="img/saw2.png" class="blog_item">

<p>Dengan :</br>
Vi	: Nilai Akhir Alternatif</br>
Wi 	: Bobot yang telah ditentukan</br>
Rij 	: Normalisasi matriks</br>
Nilai V yang lebih besar, mengindikasikan bahwa alternatif Ai lebih terpilih.</p>

<p> Sedangkan Sistem Pendukung Keputusan itu sendiri merupakan suatu kumpulan prosedur pemrosesan data dan informasi yang berorientasi pada penggunaan model untuk menghasilkan berbagai jawaban yang dapat membantu manajemen dalam pengambilan keputusan. Dengan menggunakan Sistem Pendukung Keputusan dengan metode Simple Additive Weighting, aplikasi ini dapat memberikan alternatif rekomendasi kamera berdasarkan beberapa kriteria yang telah ada. Dengan membandingkan spesifikasi dari setiap kamera diharapkan Kamu bisa mendapatkan kamera Mirrorless yang terbaik sesuai dengan budget yang Kamu miliki.
</p>

